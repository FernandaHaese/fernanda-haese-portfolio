export function SocialLinks({
  className,
  iconClassName,
  variant = "lilac",
}: {
  className?: string;
  iconClassName?: string;
  variant?: "lilac" | "outline";
}) {
  const { t } = useTranslation("common");
  const btnBase =
    "btn-hard btn-hard-hover inline-flex items-center justify-center min-h-11 min-w-11 h-11 w-11";
  const btnVariant = variant === "lilac" ? "bg-lilac text-ink" : "bg-card text-ink";

  const items = [
    {
      key: "behance",
      href: siteConfig.socialLinks.behance,
      icon: BehanceIcon,
      label: t("social.behance"),
    },
    {
      key: "linkedin",
      href: siteConfig.socialLinks.linkedin,
      icon: LinkedinIcon,
      label: t("social.linkedin"),
    },
    {
      key: "github",
      href: siteConfig.socialLinks.github,
      icon: GithubIcon,
      label: t("social.github"),
    },
  ];

  return (
    <ul className={cn("flex items-center gap-2", className)}>
      {items.map(({ key, href, icon: Icon, label }) => (
        <li key={key}>
          <a
            href={href}
            target="_blank"
            rel="noreferrer noopener"
            aria-label={label}
            className={cn(btnBase, btnVariant)}
          >
            <Icon className={cn("h-5 w-5", iconClassName)} />
          </a>
        </li>
      ))}
    </ul>
  );
}
